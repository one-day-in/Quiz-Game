const listeners = new Set();

function canUseStorage() {
  return typeof window !== 'undefined' && typeof window.localStorage !== 'undefined';
}

export function getSupportedLanguages() {
  return ['uk', 'en', 'ru'];
}

export function getLanguage() {
  if (!canUseStorage()) return DEFAULT_LANGUAGE;
  const stored = window.localStorage.getItem(LANGUAGE_KEY);
  return getSupportedLanguages().includes(stored) ? stored : DEFAULT_LANGUAGE;
}

export function setLanguage(nextLanguage) {
  const normalized = getSupportedLanguages().includes(nextLanguage) ? nextLanguage : DEFAULT_LANGUAGE;
  if (canUseStorage()) {
    window.localStorage.setItem(LANGUAGE_KEY, normalized);
  }
  if (typeof document !== 'undefined') {
    document.documentElement.lang = normalized;
  }
  listeners.forEach((listener) => listener(normalized));
  return normalized;
}

export function subscribeLanguage(listener) {
  listeners.add(listener);
  return () => listeners.delete(listener);
}

export function initLanguageFromUrl() {
  if (typeof window === 'undefined') return getLanguage();
  const lang = new URLSearchParams(window.location.search).get('lang');
  if (lang) return setLanguage(lang);
  const current = getLanguage();
  if (typeof document !== 'undefined') {
    document.documentElement.lang = current;
  }
  return current;
}

export function withLanguageParam(path) {
  const url = new URL(path, window.location.origin);
  url.searchParams.set('lang', getLanguage());
  return url.toString();
}

export function t(key, params = {}) {
  const language = getLanguage();
  const dict = dictionaries[language] || dictionaries[DEFAULT_LANGUAGE];
  const fallback = dictionaries[DEFAULT_LANGUAGE];
  const template = dict[key] ?? fallback[key] ?? key;
  return Object.entries(params).reduce(
    (result, [paramKey, paramValue]) => result.replaceAll(`{${paramKey}}`, String(paramValue)),
    template,
  );
}

export function formatLocaleDate(isoString) {
  if (!isoString) return '';
  const localeMap = { uk: 'uk-UA', en: 'en-US', ru: 'ru-RU' };
  try {
    return new Date(isoString).toLocaleString(localeMap[getLanguage()] || 'uk-UA', {
      day: '2-digit',
      month: '2-digit',
      year: 'numeric',
      hour: '2-digit',
      minute: '2-digit',
    });
  } catch {
    return '';
  }
}
